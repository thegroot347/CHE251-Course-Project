%% ============================================================
%  LEBLANC_FINAL.m
%  CHE251 - Leblanc Process Model
%  IIT Kanpur
%
%  Single-file version:
%  - Material Balance Solver
%  - Energy Balance
%  - Pretty Output
%
%  Basis:
%  F16 = 1000 kmol/h Na2CO3
%
%% ============================================================

clear;
clc;

fprintf('\n');
fprintf('============================================================\n');
fprintf('      LEBLANC PROCESS SIMULATION\n');
fprintf('============================================================\n');

%% ============================================================
% INITIAL GUESS
%% ============================================================

nVar = 50;

x0 = ones(nVar,1)*100;

% fixed values
x0(16) = 1000;      % F16
x0(25) = 2/3;       % x1
x0(29) = 1/3;       % x5

%% ============================================================
% BOUNDS
%% ============================================================

lb = zeros(nVar,1);
ub = inf(nVar,1);

% fixed product basis
lb(16)=1000;
ub(16)=1000;

% fixed fractions
lb(25)=2/3;
ub(25)=2/3;

lb(29)=1/3;
ub(29)=1/3;

% mole fractions

ub(26:28)=1;
ub(30:36)=1;
ub(37:42)=1;
ub(43:47)=1;

%% ============================================================
% SOLVER
%% ============================================================

opts = optimoptions('lsqnonlin',...
    'Display','iter',...
    'MaxIterations',2000,...
    'MaxFunctionEvaluations',100000,...
    'FunctionTolerance',1e-12);

[x,resnorm] = lsqnonlin(@leblancResiduals,...
    x0,lb,ub,opts);

fprintf('\n');
fprintf('Residual Norm = %.6e\n',resnorm);

%% ============================================================
% VARIABLE NAMES
%% ============================================================

names = {
'F','F2','F3','F4','F5','F6','F7','F8','F9','F10',...
'F11','F12','F13','F14','F15','F16','F17','F18','F19','F20',...
'F21','F22','F23','F24',...
'x1','x2','x3','x4','x5','x6','x7','x8','x9','x10','x11','x12',...
'y3','y6','y7','y9','y11','y12',...
'z3','z6','z9','w6','v6',...
'e1','e2','e3'};

%% ============================================================
% PRINT RESULTS
%% ============================================================

fprintf('\n');
fprintf('============================================================\n');
fprintf('MATERIAL BALANCE RESULTS\n');
fprintf('============================================================\n');

fprintf('------------------------------------------------------------\n');
fprintf('| %-8s | %-30s |\n','Variable','Value');
fprintf('------------------------------------------------------------\n');

for i=1:length(names)

    fprintf('| %-8s | %-30.12g |\n',names{i},x(i));

end

fprintf('------------------------------------------------------------\n');

%% ============================================================
% ENERGY BALANCE
%% ============================================================

e1 = x(48);
e2 = x(49);
e3 = x(50);

basis = x(16);

mol_per_h = basis*1000;

MW = 105.99;

tonne_per_h = basis*MW/1000;

Cp.NaCl   = 50;
Cp.H2SO4  = 84;
Cp.Na2SO4 = 100;
Cp.HCl    = 29;

Cp.CaCO3  = 82;
Cp.C      = 8;
Cp.CaS    = 50;
Cp.CO2    = 37;

Cp.Na2CO3 = 112;
Cp.H2O    = 34;

dH.R1 = -160e3;
dH.R2 = +700e3;
dH.R3 = +20e3;

T.R1 = 400;
T.R2 = 1000;
T.R3 = 60;

corr = @(dh,nu,cp,Tc) ...
    dh + sum(nu.*cp)*(Tc-25);

%% Reactor 1

nu1 = [2 1 -1 -2];

cp1 = [...
    Cp.NaCl ...
    Cp.H2SO4 ...
    Cp.Na2SO4 ...
    Cp.HCl];

DH1 = corr(dH.R1,nu1,cp1,T.R1);

Q1 = e1*1000*DH1;

%% Reactor 2

nu2 = [1 1 2 -1 -1 -2];

cp2 = [...
    Cp.Na2SO4 ...
    Cp.CaCO3 ...
    Cp.C ...
    Cp.Na2CO3 ...
    Cp.CaS ...
    Cp.CO2];

DH2 = corr(dH.R2,nu2,cp2,T.R2);

Q2 = e2*1000*DH2;

%% Leaching

nu3 = [1 1 -1];

cp3 = [...
    Cp.Na2CO3 ...
    Cp.H2O ...
    Cp.Na2CO3];

DH3 = corr(dH.R3,nu3,cp3,T.R3);

Q3 = e3*1000*DH3;

Qtotal = Q1 + Q2 + Q3;

Qtotal_GJh = Qtotal/1e9;

energy_per_tonne = Qtotal_GJh/tonne_per_h;

%% ============================================================
% ENERGY OUTPUT
%% ============================================================

fprintf('\n');
fprintf('============================================================\n');
fprintf('ENERGY BALANCE SUMMARY\n');
fprintf('============================================================\n');

fprintf('------------------------------------------------------------\n');
fprintf('| %-20s | %-12s |\n','Unit','GJ/h');
fprintf('------------------------------------------------------------\n');

fprintf('| %-20s | %12.4f |\n','Reactor I',Q1/1e9);
fprintf('| %-20s | %12.4f |\n','Reactor II',Q2/1e9);
fprintf('| %-20s | %12.4f |\n','Leaching',Q3/1e9);

fprintf('------------------------------------------------------------\n');
fprintf('| %-20s | %12.4f |\n','TOTAL',Qtotal_GJh);
fprintf('------------------------------------------------------------\n');

fprintf('\n');
fprintf('Na2CO3 Production  : %.3f tonne/h\n',tonne_per_h);
fprintf('Specific Energy   : %.4f GJ/t\n',energy_per_tonne);

fprintf('\n');
fprintf('============================================================\n');
fprintf('SIMULATION COMPLETE\n');
fprintf('============================================================\n');

%% ============================================================
% RESIDUAL FUNCTION
%% ============================================================

function L = leblancResiduals(x)

F=x(1);F2=x(2);F3=x(3);F4=x(4);F5=x(5);
F6=x(6);F7=x(7);F8=x(8);F9=x(9);F10=x(10);
F11=x(11);F12=x(12);F13=x(13);F14=x(14);
F15=x(15);F16=x(16);F17=x(17);F18=x(18);
F19=x(19);F20=x(20);F21=x(21);F22=x(22);
F23=x(23);F24=x(24);

x1=x(25);x2=x(26);x3=x(27);x4=x(28);
x5=x(29);x6=x(30);x7=x(31);x8=x(32);
x9=x(33);x10=x(34);x11=x(35);x12=x(36);

y3=x(37);y6=x(38);y7=x(39);
y9=x(40);y11=x(41);y12=x(42);

z3=x(43);z6=x(44);z9=x(45);

w6=x(46);
v6=x(47);

e1=x(48);
e2=x(49);
e3=x(50);

L=zeros(47,1);

L(1)=2*F16-F*x1;
L(2)=x1-2*(1-x1);
L(3)=F3*x3-F2*x2+2*e1;
L(4)=F3*y3-F2*(1-x2)+e1;
L(5)=F3*z3-e1;
L(6)=F3*(1-x3-y3-z3)-2*e1;
L(7)=2*e1-0.98*F2*x2;
L(8)=F+F7-F2;
L(9)=F*x1+F7*x4-F2*x2;
L(10)=F3-F4-F8-F5;
L(11)=F3*x3-F5*x4;
L(12)=F3*y3-F5*(1-x4);
L(13)=F3*z3-F8;
L(14)=F5-F7-F6;
L(15)=F*x1-2*F9*x5;
L(16)=2*x5-(1-x5);
L(17)=F11*x6-F24*y11+e2;
L(18)=F11*y6-F24*x11+e2;
L(19)=F11*z6-F24*(1-x11-y11)+2*e2;
L(20)=F11*w6-e2;
L(21)=F11*v6-e2;
L(22)=F11*(1-x6-y6-z6-w6-v6)-2*e2;
L(23)=e2-0.9*F24*y11;
L(24)=F8+F9+F10-F24;
L(25)=F8+F10*y7-F24*y11;
L(26)=F9*x5+F10*x7-F24*x11;
L(27)=F11-F12-F13-F15;
L(28)=F11*x6-F12*y7;
L(29)=F11*y6-F12*x7;
L(30)=F11*z6-F12*(1-x7-y7);
L(31)=F11*w6-F15*x8;
L(32)=F11*v6-F15*(1-x8);
L(33)=F12-F10-F14;
L(34)=0;
L(35)=F18*y9-F23*x12+e3;
L(36)=F18*x9-e3;
L(37)=F18*z9-F23*(1-x12-y12)+12.5*e3;
L(38)=F18*(1-x9-y9-z9)-F23*y12;
L(39)=F15+F16+F17-F23;
L(40)=F15*x8+F17*x10-F23*x12;
L(41)=F15*(1-x8)-F23*y12;
L(42)=e3-0.95*F23*x12;
L(43)=F18-F19-F20-F21;
L(44)=F18*x9-F20;
L(45)=F18*y9-F19*x10;
L(46)=F18*z9-F19*(1-x10);
L(47)=F19-F17-F22;

end